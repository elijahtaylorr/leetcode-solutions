class PeekingIterator(object):
    def __init__(self, iterator):
        self.iterator = iterator
        self.stored = self.iterator.next() if self.iterator.hasNext() else None        

    def peek(self):
        return self.stored
        

    def next(self):
        temp = self.stored
        self.stored = self.iterator.next() if self.iterator.hasNext() else None        
        return temp
    

    def hasNext(self):
        return self.stored is not None
        

